# Dictionary data structure, define in {}
# store values in key value pairs
# it is mutable, means changable, 
# index is not exist in dictionary, thats why it callesd unordered
# index is based on keys

marks = {"maths" : 56, "chemistry" : 45, "biology" : 67}

print(marks)

