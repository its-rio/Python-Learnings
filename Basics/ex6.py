# Operators: + - * / 
# // = floor division, decimal ke baad vali value neglect karta hai
# % = reminder operator (Modulo)
# ** = power operator (5**2)= 5 to the power 2

print(5 + 2)
print(5 - 2)
print(5 * 2)
print(5 / 2)
print(5 // 2)
print(5 % 2)
print(5 ** 2)

